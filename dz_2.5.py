prices = [
    51.9, 85.19, 50.7, 57.02, 64.22, 89.4, 69.46, 66.92, 91.35, 88.32, 85.9,
    78.17, 94.35, 73.2, 80.05
]
res = []
for i in prices:
    j = int(i)
    g = i - j
    g = round(g, 2)
    g = int(g)
    res.append(str(j) + 'руб' + ' ' + str(g) + 'коп')

print(', '.join(res))